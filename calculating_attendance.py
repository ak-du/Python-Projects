# calculating_attendance

total_class = 10
class_attend = 8

percentage_class_attended = (class_attend * 100) / total_class

if percentage_class_attended < 75:
    print('percentage of class attended is', percentage_class_attended, 'Hence, you are not allowed to sit in the exam')
else:
    print('percentage of class attended is', percentage_class_attended, 'Hence, you are allowed to sit in the exam')
